import java.util.ArrayList;

public class Pacote {

	private String nome;
	private ArrayList<Classe> listaClasses;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public ArrayList<Classe> getListaClasses() {
		return listaClasses;
	}

	public void setListaClasses(ArrayList<Classe> listaClasses) {
		this.listaClasses = listaClasses;
	}

}
